/**
 Vòng lặp while
 */
var a = 0;
while (a < 1000){
    a++;
    console.log(a);// chạy từ 1 đến 1000
}
var c = 1;
while (c <= 1000){
    console.log(c);// chạy 1 đến 1000
    c++;
}

// dùng vòng lặp while lấy ra phần tử trong object
var myarray =[
    "javascript",
    "python",
    "css"
]
var i = 0;
while (i < myarray.length){
    console.log(myarray[i]);
    i++;
}























